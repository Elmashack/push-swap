#include "libft.h"
int	ft_isalnum(int c)
{
	if ((('z' >= c) && (c >= 'a')) || (('Z' >= c && c >= 'A')))
		return (1);
	else if (('9' >= c) && (c >= '0'))
		return (1);
	else
		return (0);
}
